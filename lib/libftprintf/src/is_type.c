#include <printf_utils.h>

int	is_type(char ty)
{
	if (ty == 'd' || ty == 'D' || ty == 's' || ty == 'S' || ty == 'x' ||
		ty == 'X' || ty == 'p' || ty == 'o' || ty == 'O' || ty == 'c' ||
		ty == 'C' || ty == 'u' || ty == 'U' || ty == 'i' || ty == 'b' ||
		ty == 'f')
		return (1);
	return (0);
}
