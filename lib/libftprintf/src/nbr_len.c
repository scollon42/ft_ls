#include <printf_utils.h>

int		nbr_len(long long int n)
{
	int		len;

	len = 1;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	while ((n /= 10) > 0)
		len++;
	return (len);
}
