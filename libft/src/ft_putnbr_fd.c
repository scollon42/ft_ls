#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	r;

	r = (long)n;
	if (fd >= 0)
	{
		if (n < 0)
		{
			r *= -1;
			ft_putchar_fd('-', fd);
		}
		if (r > 9)
			ft_putnbr_fd(r / 10, fd);
		ft_putchar_fd((r % 10) + '0', fd);
	}
}
