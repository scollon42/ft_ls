#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	c = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(1, "-", 1);
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
		{
			c = n + 48;
			write(1, &c, 1);
		}
	}
}
