#include "libft.h"

void	ft_putnbr(int n)
{
	long	r;

	r = (long)n;
	if (n < 0)
	{
		r *= -1;
		ft_putchar('-');
	}
	if (r > 9)
		ft_putnbr(r / 10);
	ft_putchar((r % 10) + '0');
}
